drop policy if exists "Forum topics are readable" on public.forum_topics;
create policy "Forum topics are readable"
on public.forum_topics
for select
to anon, authenticated
using (coalesce(oculto, false) = false);

drop policy if exists "Anyone can create visible topics" on public.forum_topics;
create policy "Anyone can create visible topics"
on public.forum_topics
for insert
to anon, authenticated
with check (
  category_id is not null
  and length(btrim(titulo)) > 0
  and length(btrim(conteudo)) > 0
  and (nota is null or (nota >= 1 and nota <= 5))
  and coalesce(oculto, false) = false
  and coalesce(fixado, false) = false
  and coalesce(trancado, false) = false
  and (user_id is null or (select auth.uid()) = user_id)
);

drop policy if exists "Forum replies are readable" on public.forum_replies;
create policy "Forum replies are readable"
on public.forum_replies
for select
to anon, authenticated
using (coalesce(oculto, false) = false);

drop policy if exists "Anyone can create visible replies" on public.forum_replies;
create policy "Anyone can create visible replies"
on public.forum_replies
for insert
to anon, authenticated
with check (
  topic_id is not null
  and length(btrim(conteudo)) > 0
  and coalesce(oculto, false) = false
  and (user_id is null or (select auth.uid()) = user_id)
  and exists (
    select 1
    from public.forum_topics topic
    where topic.id = forum_replies.topic_id
      and coalesce(topic.oculto, false) = false
      and coalesce(topic.trancado, false) = false
  )
);
