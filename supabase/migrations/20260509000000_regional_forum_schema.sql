create table if not exists public.forum_categories (
  id bigint generated by default as identity primary key,
  nome text not null,
  slug text not null unique,
  descricao text,
  parent_id bigint references public.forum_categories(id),
  estado text,
  tipo text,
  clinic_id bigint references public.clinicas(id),
  created_at timestamptz default now()
);

create table if not exists public.forum_topics (
  id bigint generated by default as identity primary key,
  category_id bigint references public.forum_categories(id),
  clinic_id bigint references public.clinicas(id),
  titulo text not null,
  slug text not null unique,
  conteudo text not null,
  autor text default 'Anônimo',
  user_id uuid references auth.users(id),
  nota numeric,
  views integer default 0,
  pinned boolean default false,
  locked boolean default false,
  fixado boolean default false,
  trancado boolean default false,
  oculto boolean default false,
  created_at timestamptz default now(),
  updated_at timestamptz default now(),
  constraint forum_topics_nota_range check (nota is null or (nota >= 1 and nota <= 5))
);

create table if not exists public.forum_replies (
  id bigint generated by default as identity primary key,
  topic_id bigint references public.forum_topics(id),
  conteudo text not null,
  autor text default 'Anônimo',
  user_id uuid references auth.users(id),
  oculto boolean default false,
  created_at timestamptz default now()
);

