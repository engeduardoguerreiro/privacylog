insert into public.forum_categories (nome, slug, descricao, estado)
values
  ('São Paulo', 'sao-paulo', 'Discussões e avaliações em São Paulo.', 'SP'),
  ('Minas Gerais', 'minas-gerais', 'Discussões e avaliações em Minas Gerais.', 'MG'),
  ('Rio de Janeiro', 'rio-de-janeiro', 'Discussões e avaliações no Rio de Janeiro.', 'RJ'),
  ('Paraná', 'parana', 'Discussões e avaliações no Paraná.', 'PR'),
  ('Santa Catarina', 'santa-catarina', 'Discussões e avaliações em Santa Catarina.', 'SC'),
  ('Rio Grande do Sul', 'rio-grande-do-sul', 'Discussões e avaliações no Rio Grande do Sul.', 'RS')
on conflict (slug) do update
set descricao = excluded.descricao,
    estado = excluded.estado;

with states as (
  select id, estado
  from public.forum_categories
  where parent_id is null
    and estado in ('SP', 'MG', 'RJ', 'PR', 'SC', 'RS')
),
kinds as (
  select *
  from (values
    ('clinica', 'Clínicas', 'clinicas'),
    ('massagem', 'Massagens', 'massagens'),
    ('boate', 'Boates', 'boates'),
    ('prive', 'Privês', 'prives')
  ) as kind(tipo, nome, slug_part)
)
insert into public.forum_categories (nome, slug, descricao, parent_id, estado, tipo)
select
  kinds.nome,
  lower(states.estado) || '-' || kinds.slug_part,
  kinds.nome || ' em ' || states.estado,
  states.id,
  states.estado,
  kinds.tipo
from states
cross join kinds
on conflict (slug) do update
set parent_id = excluded.parent_id,
    estado = excluded.estado,
    tipo = excluded.tipo;

with clinic_rows as (
  select
    id,
    nome,
    estado,
    tipo,
    regexp_replace(
      regexp_replace(
        translate(
          lower(coalesce(nome, 'local')),
          'áàâãäéèêëíìîïóòôõöúùûüçñ',
          'aaaaaeeeeiiiiooooouuuucn'
        ),
        '[^a-z0-9]+',
        '-',
        'g'
      ),
      '(^-|-$)',
      '',
      'g'
    ) as slug_nome
  from public.clinicas
  where estado in ('SP', 'MG', 'RJ', 'PR', 'SC', 'RS')
    and tipo in ('clinica', 'massagem', 'boate', 'prive')
),
parents as (
  select id, estado, tipo
  from public.forum_categories
  where parent_id is not null
    and clinic_id is null
),
inserted_categories as (
  insert into public.forum_categories (
    nome,
    slug,
    descricao,
    parent_id,
    clinic_id,
    estado,
    tipo
  )
  select
    clinic_rows.nome,
    clinic_rows.tipo || '-' || clinic_rows.id || '-' || coalesce(nullif(clinic_rows.slug_nome, ''), 'local'),
    'Discussões e avaliações sobre ' || clinic_rows.nome,
    parents.id,
    clinic_rows.id,
    clinic_rows.estado,
    clinic_rows.tipo
  from clinic_rows
  join parents
    on parents.estado = clinic_rows.estado
   and parents.tipo = clinic_rows.tipo
  on conflict (slug) do update
  set nome = excluded.nome,
      descricao = excluded.descricao,
      parent_id = excluded.parent_id,
      clinic_id = excluded.clinic_id,
      estado = excluded.estado,
      tipo = excluded.tipo
  returning id, clinic_id
)
update public.clinicas
set forum = '/forum/categoria/' || inserted_categories.id
from inserted_categories
where public.clinicas.id = inserted_categories.clinic_id;

update public.forum_topics
set clinic_id = forum_categories.clinic_id
from public.forum_categories
where public.forum_topics.category_id = forum_categories.id
  and public.forum_topics.clinic_id is null
  and forum_categories.clinic_id is not null;

